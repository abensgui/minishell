#include "../../minishell.h"

int	check_all_key(char *s)
{
	int	i;

	i = 1;
	while (s[i])
	{
		if ((s[i] >= 65 && s[i] <= 90)
			|| (s[i] >= 97 && s[i] <= 122)
			|| s[i] == 95
			|| (s[i] >= 48 && s[i] <= 57))
			i++;
		else
			return (0);
	}
	return (1);
}
