#include "../../minishell.h"

int	strfind(char *s, char c)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (s[i] == c)
		{
			if (s[i + 1] == '=')
				return (1);
		}
		i++;
	}
	return (0);
}
